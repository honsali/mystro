package app.common.model;

public final class NativePlanetaryHour {
    private String summary;
    private int hourNumber;
    private String dayRuler;
    private String nightRuler;
    private String hourRuler;
    private Double last;
    private Double next;



    public String summary() {
        return summary;
    }

    public void summary(String summary) {
        this.summary = summary;
    }

    public String getSummary() {
        return summary;
    }

    public void setSummary(String summary) {
        this.summary = summary;
    }

    public int hourNumber() {
        return hourNumber;
    }

    public void hourNumber(int hourNumber) {
        this.hourNumber = hourNumber;
    }

    public int getHourNumber() {
        return hourNumber;
    }

    public void setHourNumber(int hourNumber) {
        this.hourNumber = hourNumber;
    }

    public String dayRuler() {
        return dayRuler;
    }

    public void dayRuler(String dayRuler) {
        this.dayRuler = dayRuler;
    }

    public String getDayRuler() {
        return dayRuler;
    }

    public void setDayRuler(String dayRuler) {
        this.dayRuler = dayRuler;
    }

    public String nightRuler() {
        return nightRuler;
    }

    public void nightRuler(String nightRuler) {
        this.nightRuler = nightRuler;
    }

    public String getNightRuler() {
        return nightRuler;
    }

    public void setNightRuler(String nightRuler) {
        this.nightRuler = nightRuler;
    }

    public String hourRuler() {
        return hourRuler;
    }

    public void hourRuler(String hourRuler) {
        this.hourRuler = hourRuler;
    }

    public String getHourRuler() {
        return hourRuler;
    }

    public void setHourRuler(String hourRuler) {
        this.hourRuler = hourRuler;
    }

    public Double last() {
        return last;
    }

    public void last(Double last) {
        this.last = last;
    }

    public Double getLast() {
        return last;
    }

    public void setLast(Double last) {
        this.last = last;
    }

    public Double next() {
        return next;
    }

    public void next(Double next) {
        this.next = next;
    }

    public Double getNext() {
        return next;
    }

    public void setNext(Double next) {
        this.next = next;
    }


}
